const interpolate = (template: string, params?: Record<string, string | number>) => {
  if (!params) return template;
  return template.replace(/\{\{\s*(\w+)\s*\}\}/g, (_, token) =>
    params[token] !== undefined ? String(params[token]) : '',
  );
};

export const getTranslation = (
  lang: LanguageCode,
  key: string,
  params?: Record<string, string | number>,
): string => {
  const languagePack = translations[lang] ?? translations[fallbackLanguage];
  const fallbackPack = translations[fallbackLanguage];
  const template = languagePack[key] ?? fallbackPack[key] ?? key;
  return interpolate(template, params);
};
